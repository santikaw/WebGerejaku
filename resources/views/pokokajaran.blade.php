                    <p>Bahwa timbul ketidaksetujuan dari sebagian warga gereja, haruslah diterima sebagai kenyataan di lapangan, oleh karena tidak seluruh warga GKJ siap untuk berolah-fikir secara modern secara serentak dan serta merta. Menjadi penting bagi GKJ untuk memberi peluang bagi usaha untuk saling mengerti. Harus diakui bahwa ada tahap-tahap berfikir dalam sejarah kebudayaan, dan perbedaan-perbedaan tahap berfikir ini mempengaruhi cara orang memahami masalah-masalah.
                      Oleh sebab itu dikembangkan usaha untuk memahami dan saling memahami, sehingga PPA GKJ 1996 dapat menjadi alat bantu dalam memacu GKJ menjadi saksi yang lebih berdaya guna pada awal Abad ke-21 ini.</p>
                  </div>
                </div>
                <table class="container" style="margin-top: 25px;">
                  <tr>
                    <td><b> 8. Perkembangan Tahap-tahap Berfikir dalam Sejarah Kebudayaan </b></td>
                  </tr>
                </table>
                <div class="container">
                  <div style="margin-top: 25px;">
                    <p>Prof. Dr. C.A. van Peursen, dalam bukunya: Strategi Kebudayaan mengetengahkan upaya untuk memahami perkembangan cara berfikir manusia melalui suatu bagan tiga tahap. Adapun tahap-tahap yang dimaksudkan adalah tahap mitis, tahap ontologis dan tahap fungsional.</p>
                  </div>
                </div>
                <div class="container">
                  <div style="margin-top: 25px;">
                    <p>Tahap mitis tercermin dalam mitologi-mitologi dari bangsa-bangsa yang sering dinamakan bangsa primitif, yang pada dasarnya mengungkapkan sikap manusia yang merasakan dirinya terkepung oleh kekuatan-kekuatan gaib di sekitarnya, yaitu kekuasaan dewa-dewa alam raya atau kekuasaan kesuburan. Alam fikiran mitis bergetar apabila berhadapan dengan daya purba dan mengakui bahwa ada sesuatu.
                      Manusia merasa dirinya merupakan bagian dari keseluruhan yang mengitarinya, pola pikir yang dikembangkannya bersifat partisipatif. Mantera dan magi merupakan hal dominan pada tahap ini.</p>
                  </div>
                </div>
                <div class="container">
                  <div style="margin-top: 25px;">
                    <p>Tahap ontologis mengungkapkan sikap manusia yang tidak lagi hidup dalam kepungan kekuasaan mitis, melainkan yang secara bebas ingin meneliti segala hal ikhwal. Manusia mengambil jarak terhadap segala sesuatu yang dulu dirasakan sebagai kepungan. Ia lalu menyusun teori mengenai dasar hakekat segala sesuatu itu (ontologi), dan rincian-rincian dari segala sesuatu itu (=ilmu-ilmu).
                      Tahap ini berkembang dalam Kebudayaan Kuno yang sangat dipengaruhi oleh filsafat dan ilmu pengetahuan. Di Barat misalnya pada zaman Yunani Kuno tatkala filsuf-filsuf yang terkenal berkarya, yaitu Anaxagoras, Sokrates, Plato dan Aristoteles; sementara di Timur beberapa mazhab Vedanta dari India mencerminkan tahap ini..</p>
                  </div>
                </div>
                <div class="container">
                  <div style="margin-top: 25px;">
                    <p>Pada tahap ini manusia berusaha membuat peta mengenai segala sesuatu, menggali sebab musabab terjadinya segala sesuatu, menyajikan pengetahuan sistematis yang dapat dikontrol. Manusia sebagai subyek mengambil jarak (distansi) dari segala sesuatu yang menjadi obyek penelitiannya. Manusia berusaha mengetahui mengenai hakikat segala sesuatu, mengetahui apa-nya. Dalam diskusi teologi mengenai ontologi tradisional,
                      orang berusaha untuk membuktikan adanya Tuhan. Tuhan dikaji tentang hakekat dan keberadaanNya lepas dari subyek manusia, lepas dari kebertautan langsung dengan eksistensi yang ia hayati.</p>
                  </div>
                </div>
                <div class="container">
                  <div style="margin-top: 25px;">
                    <p>Tahap fungsional, nampak dalam manusia modern. Ia tidak terpesona oleh lingkungannya (=mitis), ia tidak lagi dengan kepala dingin mengambil jarak terhadap obyek penyelidikannya (=ontologis), tetapi ia ingin mengadakan relasi-relasi baru dengan segala sesuatu dalam lingkungannya. Ia mengutamakan pertanyaan : bagaimana dalam kaitan dengan segala sesuatu; ia tidak bertanya mengenai adanya barang-barang itu,
                      tetapi bertanya mengenai artinya bagi dia, yaitu cara sesuatu itu dialami dan diintegrasikan dalam hidupnya. Manusia ingin mengubah dunia, kehidupan sosial ditandai oleh unsur arti dan pengelolaan. Ia menyukai sistem yang terbuka, segala sesuatu dilihat bukan sebagai sesuatu yang bulat dan tertutup (=ontologis), tetapi sebagai yang selalu bergerak, sebagai proses.</p>
                  </div>
                </div>
                <div class="container">
                  <div style="margin-top: 25px;">
                    <p>Bagan tiga tahap atau ketiga sikap dasar seperti diuraikan di atas sebenarnya hanya merupakan suatu skema, atau sebuah sarana untuk membantu kita. Kalau disebut tahap tidak lalu diartikan tahap secara harafiah, yaitu perkembangan bertingkat di mana tahap yang satu digantikan oleh tahap berikutnya. Pada kenyataannya kita tidak boleh lupa bahwa semua tahap perkembangan cara berfikir manusia itu terdapat dalam kita semua,
                      bahkan dalam kita masing-masing. Yang dipentingkan dalam bagan ini ialah aksen-aksen yang bergeser, strategi-strategi yang berbeda-beda dari masing-masing tahap. Apa yang disebut sebagai manusia primitif dengan dongeng-dongeng mitisnya, maklum juga mengenai hal-hal yang praktis-teknis, dia pun dapat mendekati sesuatu secara fungsional. Sebaliknya kita dalam masyarakat modern tidak lepas dari unsur-unsur magis.
                      Kita pun dapat dipengaruhi oleh mitos-mitos pengarang-pengarang besar yang serba mendalam atau oleh ideologi-ideologi politis. Sekalipun ada kemajuan-kemajuan teknis, medis dan ilmiah, tetapi sejarah kebudayaan manusia tidak dengan sendirinya memperlihatkan suatu garis yang menanjak (linier).</p>
                  </div>
                </div>
                <table class="container" style="margin-top: 25px;">
                  <tr>
                    <td><b> 9. Penutup </b></td>
                  </tr>
                </table>
                <div style="margin-top: 25px;">
                  <p> Seperti telah disinggung di atas, PPA GKJ 1996 telah dipersiapkan dan disusun untuk memenuhi kebutuhan GKJ yang hidup di zaman modern, sehingga lebih memberikan tekanan pada pendekatan fungsional. Ketritunggalan Allah oleh sebagian besar warga GKJ tetap dirasakan perlu untuk disebut. Di dalam PPA GKJ 1996 hal tersebut disajikan bukan dalam bentuk ulangan rumusan-rumusan klasik dari Konsili-konsili Nicea-Konstantinopel (Abad ke-4 M.).
                    Kristus tidak lagi diuraikan mengikuti rumusan Konsili Chalcedon (Abad ke-5 M.) yang sesuai dengan perkembangan pemikiran pada zaman itu yang bersifat ontologis, tetapi diuraikan secara baru sesuai dengan pendekatan fungsional. Dengan cara demikian, dialog dengan masyarakat luas, yang sebelumnya sulit memahaminya, diharapkan dapat lebih mudah dilakukan. Sebab Ketritunggalan Allah itu lebih dikaitkan pada Allah yang berkarya bagi keselamatan manusia.
                    [Lihat PPA GKJ 1996 Pertanyaan-Jawaban (P-J) 52; buku ini: P-J 42].
                  </p>
                </div>
                <div style="margin-top: 25px;">
                  <p> Gereja juga diuraikan secara fungsional, dengan diawali oleh suatu uraian dengan memperhatikan fenomenologi Agama, lalu diteruskan dengan uraian lanjutan yang memperhatikan hubungan dengan masyarakat keagamaan Indonesia yang bersifat majemuk (Lih. P-J 81 dst., 242 dst.; buku ini: P-J 71 dst., 202 dst.). Selanjutnya PPA GKJ mendedikasikan Minggu ke-9 untuk membahas Fungsi Gereja (buku ini: Tugas Panggilan Gereja).
                  </p>
                </div>
                <div style="margin-top: 25px;">
                  <p> Perhatian terhadap relasi/hubungan antara kehidupan orang percaya dengan dunia, alam, negara, ilmu pengetahuan dan teknik diberi tempat secara panjang lebar dalam PPA GKJ. P-J 152 (buku ini: P-J 12, 32, 33) menjelaskan bahwa penyelamatan Allah berlangsung dalam anyaman bersama dengan kehidupan manusia di dunia. P-J 175 dst. (buku ini: P-J 150 dst.) menjelaskan mengenai menyikapi masalah hubungan manusia dengan alam dan tugas manusia sebagai mandataris
                    Allah terhadap alam yang harus dilakukan dengan bertanggungjawab. P-J 191 (buku ini: P-J 167) membicarakan mengenai bagaimana orang percaya memfungsikan akal budinya dalam mengolah ilmu pengatahuan, teknologi dan teknik. P-J 194 (buku ini: P-J 169) membahas bagaimana fungsi iman itu dibutuhkan agar manusia bermartabat manusia. P-J 216 (buku ini: P-J 186) membicarakan mengenai fungsi dasar kekuasaan negara. P-J 225-227 (buku ini: P-J 187) membahas mengenai martabat manusia dan Hak-hak Asasi Manusia. Contoh-contoh di atas menjelaskan tentang bagaimana pendekatan fungsional sangat mengemuka dalam PPA GKJ ini.
                  </p>
                </div>
                <div style="margin-top: 25px;">
                  <p> Namun, seperti juga telah diuraikan di atas, tidak semua warga GKJ dapat mengikuti alur fikiran tahap fungsional dengan serta merta. Ada yang belum mampu mengubah paradigma berfikir sehingga masih ingin mempertahankan yang lama. Menjadi penting bagi semua warga GKJ untuk memberi tempat kepada perbedaan-perbedaan cara memahami kebenaran, tanpa mengorbankan inti iman Kristen, sementara tetap memelihara ikatan cinta kasih, sebagai warga keluarga Allah dalam Kristus.
                  </p>
                </div>
                <div style="margin-top: 25px;">
                  <p>
                    Dapat dicatat bahwa akhir-akhir ini sebagai reaksi terhadap filsafat modern muncul aliran filsafat yang di disebut filsafat post-modern (=pasca modern). Aliran ini bersikap kritis terhadap netralitas dan kedaulatan akal budi. Ia menolak asumsi adanya “kata-kata terakhir yang menentukan”, yaitu menolak rumusan prinsip-prinsip, pembedaan-pembedaan dan kategori-kategori, yang dipandang mengikat tanpa syarat, bagi segala waktu, orang dan tempat. Ia juga menolak “mimpi tradisional” mengenai adanya sistem penjelasan yang lengkap, unik dan tertutup (cf. Cambridge Dictionary of Philosophy, s.v.”Postmodernism”).
                  </p>
